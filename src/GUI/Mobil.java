    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        String tNoPol = txtNoPolisi.getText();
        String tMerk = txtMerk.getText();
        String tJns = txtJenis.getText();
        String tWrn = txtWarna.getText();
        double trf=Double.parseDouble(txtTarif.getText());
        try{
            if (edit==true)
            {
                stm.executeUpdate("update mobil set merk='"+tMerk+"',jenis = '"+tJns+"',warna = '"+tWrn+"',tarif="+trf+" where no_polisi='"+tNoPol+"'");
            }else
            {
                stm.executeUpdate("INSERT into mobil VALUES('"+tNoPol+"','"+tMerk+"','"+tJns+"','"+tWrn+"',"+trf+")");
            }
            //tblMobil.setModel(new DefaultTableModel(dataTable,header));
            baca_data();
            aktif(false);
            setTombol(true);
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanMouseClicked

