    private void baca_data() {
        try{
            stm = Con.createStatement();
            RsMbl = stm.executeQuery("select * from mobil");
                
            ResultSetMetaData meta = RsMbl.getMetaData();
            int col = meta.getColumnCount();            
            int baris = 0;
            while(RsMbl.next()) {
                baris = RsMbl.getRow();
            }
            
            dataTable = new Object[baris][col];
            int x = 0;
            RsMbl.beforeFirst();
            while(RsMbl.next()) {
                dataTable[x][0] = RsMbl.getString("no_polisi");
                dataTable[x][1] = RsMbl.getString("merk");
                dataTable[x][2] = RsMbl.getString("jenis");
                dataTable[x][3] = RsMbl.getString("warna");
                dataTable[x][4] = RsMbl.getDouble("tarif");
                x++;
            }
            tblMobil.setModel(new DefaultTableModel(dataTable,header));        
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

